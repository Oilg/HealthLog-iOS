import Foundation

/// Каталог пользовательских объяснений для каждого типа риска или прогноза.
///
/// Серверная сторона запускает детекторы и возвращает `RiskItem` с полем
/// `type`. Каталог отвечает за то, что пользователь читает на экране
/// деталей: объяснение, шаги, к какому врачу обратиться, какие анализы
/// попросить, какие данные использовались.
///
/// Контент собран по категориям в отдельные файлы расширений
/// (`RiskInfoCatalog+*.swift`), чтобы не превышать лимит строки на файл
/// и упростить медицинский ревью.
enum RiskInfoCatalog {
    /// Возвращает карточку для известного типа риска. Для неизвестного
    /// типа возвращает консервативный дефолт — на случай, если сервер
    /// добавит новый тип раньше, чем приложение обновится.
    static func info(for type: String) -> RiskInfo {
        if let info = cardiovascular(type) { return info }
        if let info = metabolic(type) { return info }
        if let info = fitness(type) { return info }
        if let info = bodyComposition(type) { return info }
        if let info = menstrual(type) { return info }
        if let info = environmental(type) { return info }
        return defaultInfo
    }

    /// Консервативный fallback: направляет к терапевту без конкретных
    /// рекомендаций, чтобы не давать необоснованных медицинских советов.
    static let defaultInfo = RiskInfo(
        explanation: "Алгоритм обнаружил отклонение в одном или нескольких показателях здоровья. " +
            "Рекомендуется проконсультироваться с врачом для уточнения.",
        doctorType: "Терапевт",
        actions: ["Проконсультироваться с врачом для оценки ситуации"],
        doctorVisitReason: "«Приложение для мониторинга здоровья обнаружило потенциальное отклонение. " +
            "Хочу получить профессиональную оценку.»",
        testsToOrder: [],
        healthDataTypes: ["Данные Apple Health"]
    )
}
